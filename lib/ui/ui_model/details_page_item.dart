class DetailsItem {
  String name;
  String status;
  String species;
  String type;
  String gender;
  String originName;
  String locationName;
  String image;
  List<String> episodes;

  DetailsItem({
      required this.name,
      required this.status,
      required this.species,
      required this.type,
      required this.gender,
      required this.originName,
      required this.locationName,
      required this.image,
      required this.episodes
    }
  );
}